package model;

/**
 *
 * @author cipri
 */
public class Cliente {
    private String nome;
    private String CPF;
    private int idade;
    private int id;

    public Cliente() {
    }

    public Cliente(String nome, String CPF, int idade, int id) {
        this.nome = nome;
        this.CPF = CPF;
        this.idade = idade;
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCPF() {
        return CPF;
    }

    public void setCPF(String CPF) {
        this.CPF = CPF;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
